<p>This page contains an updated browser of CoreLex, a lexicon of nouns structured around systematic polysemy developed by Paul Buitelaar in his dissertation.</p>

<blockquote>
   Paul Buitelaar,<br/>
   <i>CoreLex: Systematic Polysemy and Underspecification</i>,<br/>
   PhD Thesis, Computer Science, Brandeis University, February 1998.
</blockquote>
  
<p>CoreLex is available at for download at <a href="http://www.cs.brandeis.edu/~paulb/CoreLex/corelex.html">http://www.cs.brandeis.edu/~paulb/CoreLex/corelex.html</a>, which also has a link to an old CoreLex Browser.</p>

<p>The browser here has the following components, accessible via the links at the top of this page:</p>

<ol>

   <li>A list of all the CoreLex types. Each type is clickable and leads to a page that also includes all nouns of that type. For each noun there is a link to the current WordNet (version 3.1).</li>

   <li>A list of basic types dervied from WordNet 1.5.</li>

   <li>A search function where you can enter a noun and which will result the CoreLex type for that noun.</li>

</ol>

